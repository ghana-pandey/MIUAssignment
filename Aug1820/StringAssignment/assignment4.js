'use-script'
function switchCase(s) {
    let s1;
    for (let i = 0; i < s.length; i++) {
        if (s.charAt[i] === s.toLowerCase());
        {
            s1 = s.toUpperCase();
        }
        if (s.charAt[i] === s.toUpperCase());
        {
            let s1 = s.toLowerCase();
        }

    }
    return s1;
}
